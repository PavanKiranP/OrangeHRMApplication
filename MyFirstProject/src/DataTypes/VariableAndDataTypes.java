package DataTypes;

public class VariableAndDataTypes {
	
 public static void main(String[]args) {
	
	 byte v1=5;
	 System.out.println("the value of the v1 :- "+v1);
	 
	 short v2=50;
	 System.out.println("the value of the v1 :- "+v2);
	 
	 long v3=5000;
	 System.out.println("The value of v3 :- "+v3);
	 
	 double v4=10.123456789123456789;
	 System.out.println("The value of the v4 :- "+v4);
	 
	 float v5=10.123456789123456789f;
	 System.out.println("The value of the v5 :- "+v5);
	 
	 int v6='f';
	 System.out.println("The value of the v6 :- "+v6);
	 
	 int v7=21;
	 System.out.println("The value of the v7 :- "+v7);
	 
	 char v8='d';
	 System.out.println("The value of v8 :- "+v8);
	 
	 String v9="Pulipati.SaiPavanKiran";
	 System.out.println("The value of v9 :- "+v9);
	 
	 boolean v10=true;
	 System.out.println("The value of v10 :- "+v10);
 }

}
