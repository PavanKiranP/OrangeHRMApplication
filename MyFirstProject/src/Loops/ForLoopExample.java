package Loops;

public class ForLoopExample {
	
	public static void main(String[]args) {
		
		for (int a=1;a<3;a++)
		{
			System.out.println(a+"Selenium");
		}
		
		System.out.println("********************");
		
		for (int i=2;i<8;i=i+2)
		{
			System.out.println(i+"Testing");
		}
		
		System.out.println("####################");
		
		for (int k=5;k>1;k--)
		{
			System.out.println(k+"LiveTech");
		}
		
		System.out.println("&&&&&&&&&&&&&&&&&&&&");
		
		for (int j=-2;j>-5;j--)
		{
			System.out.println(j+"Java");
		}
		
		System.out.println("====================");
		
		for (int p=-18;p>-20;p=p-2)
		{
			System.out.println(p+"LoadRunner");
		}
		
		System.out.println("--------------------");
		
		for (int l=-23;l>=-35;l=l-3)
		{
			System.out.println(l+"Jmester");
		}
	}


}
