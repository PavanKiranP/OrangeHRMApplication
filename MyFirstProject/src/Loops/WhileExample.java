package Loops;

public class WhileExample {

	public static void main(String[] args) {
		
		int a=2;
		while (a<=5)
		{
			System.out.println(a+"Selenium");
			a++;
		}
		System.out.println("&&&&&&&&&&&&&");
		int k=-5;
		while (k>-10)
		{
			System.out.println(k+"Testing");
			k--;
		}
		
		System.out.println("*****************");
		
		int p=-8;
		while (p>=-22)
		{
			System.out.println(p+"LoadRunner");
			p--;
		}

	}

}
